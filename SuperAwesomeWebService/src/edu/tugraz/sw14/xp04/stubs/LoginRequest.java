package edu.tugraz.sw14.xp04.stubs;

public class LoginRequest {

	private String id;
	private String password;

	public LoginRequest() {
		super();
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

}
